package com.example.clientbenchmark;

import java.util.UUID;

public class UplinkEvents {

    private String id;
    private String deviceDevEui;
    private String applicationId;
    private String metricId;
    private String metricValue;
    private String metricType;
    private String metricName;
    private String eventTime;

    public UplinkEvents(String deviceDevEui, String applicationId, String metricId, String metricValue, String metricType, String metricName, String eventTime) {
        this.id = UUID.randomUUID().toString(); // UUID v4 is generated by default using randomUUID()
        this.deviceDevEui = deviceDevEui;
        this.applicationId = applicationId;
        this.metricId = metricId;
        this.metricValue = metricValue;
        this.metricType = metricType;
        this.metricName = metricName;
        this.eventTime = eventTime;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDeviceDevEui() {
        return deviceDevEui;
    }

    public void setDeviceDevEui(String deviceDevEui) {
        this.deviceDevEui = deviceDevEui;
    }

    public String getApplicationId() {
        return applicationId;
    }

    public void setApplicationId(String applicationId) {
        this.applicationId = applicationId;
    }

    public String getMetricId() {
        return metricId;
    }

    public void setMetricId(String metricId) {
        this.metricId = metricId;
    }

    public String getMetricValue() {
        return metricValue;
    }

    public void setMetricValue(String metricValue) {
        this.metricValue = metricValue;
    }

    public String getMetricType() {
        return metricType;
    }

    public void setMetricType(String metricType) {
        this.metricType = metricType;
    }

    public String getMetricName() {
        return metricName;
    }

    public void setMetricName(String metricName) {
        this.metricName = metricName;
    }

    public String getEventTime() {
        return eventTime;
    }

    public void setEventTime(String eventTime) {
        this.eventTime = eventTime;
    }
}
